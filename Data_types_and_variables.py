# Integer
b = 9223372036854775807
print(b)
print(type(b))

# Floating point
pi = 3.14
print(pi)
print(type(pi))

# String
c = 'A'
print(c)
print(type(c))

# String
name = 'John Doe'
print(name)
print(type(name))

# Boolean
q = True
print(q)
print(type(q))

# Empty value or null data type
x = None
print(x)
print(type(x))

# Reference
a, b, c = 1, 2, 3
print(a, b, c)
